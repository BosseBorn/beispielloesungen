public class Rechenoperationen {

	public static void main(String[] args) {

		int i1 = 1000000;
		int i2 = 1000000;
		System.out.println(i1 + " * " + i2 + " = " + i1 * i2);

		int i3 = 15;
		int i4 = 4;
		System.out.println(i3 + " / " + i4 + " = " + i3 / i4);

		double d1 = 1E200;
		double d2 = 1E200;
		System.out.println(d1 + " * " + d2 + " = " + d1 * d2);

		double d3 = 0.1;
		double d4 = 0.1;
		System.out.println(d3 + " * " + d4 + " = " + d3 * d4);

	}
}
